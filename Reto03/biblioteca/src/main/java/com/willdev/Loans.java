package com.willdev;

public class Loans {

    private String idUser;
    private String idBook;

    public Loans(String idUser, String idBook) {
        this.idUser = idUser;
        this.idBook = idBook;
    }

    public String getIdUser() {
        return idUser;
    }

    public String getIdBook() {
        return idBook;
    }


    

}
